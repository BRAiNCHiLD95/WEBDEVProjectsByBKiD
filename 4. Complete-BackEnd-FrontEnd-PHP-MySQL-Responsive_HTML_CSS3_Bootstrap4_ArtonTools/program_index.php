                <h4>Methodology</h4>
                <p class="text-justify my-3">
                    The Arton Index measures how appealing global residence and citizenship programs are to high net worth individuals. It is composed of five pillars. These principal dimensions by which programs can be assessed are divided into the industry-related categories of Speed, Cost and Simplicity, and the general categories of Quality of Life and Global Mobility.<br><br>
                    Each category and its components make up the individual score of a program. The maximum score that a program can achieve is 100 points. The methodology is constructed in a way to allow the application of an assessment grid that reflects the program type as the index is applied to both residence to citizenship programs and direct citizenship programs.<br><br>
                    The index examines all countries that are considered major representatives of the global citizenship industry. As on the last update, the countries assessed included the following: Antigua & Barbuda, Australia, Bulgaria, Canada, Cyprus, Dominica, Greece, Grenada, Hungary, Latvia, Malta, New Zealand, Portugal, Singapore, Spain, St. Kitts & Nevis, the U.K. and the USA. The list may be enriched in time.
